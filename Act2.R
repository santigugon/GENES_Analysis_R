#7 Funcion para encontrar codones de inicio y fin

cambiar_a_codones<-function(sec){
  tam_secuencia<-length(sec)
  codones<-c(tam_secuencia/3)
  tam_codones<-length(codones)
  
  for(i in 1:tam_secuencia/3){
    for(a in 0:2){
      if(a==0){
      codon1<-sec[a+i]
      }
      if(a==1){
        codon2<-sec[a+i]
      }
      if(a==2){
        codon3<-sec[a+i]
      }
     
    }
    codones[i]<-paste(codon1,codon2,codon3,sep="")
    
  }
  return(codones)
  print(codones)
}
codon<-cambiar_a_codones(mrna)
