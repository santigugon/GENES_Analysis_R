# 1. Crea una función que genere una secuencia aleatoria de nucleótidos de ADN (A, T,
#G y C) de tamaño “n”.
#Ejecútala solicitando una secuencia aleatoria de ADN de 30 nucleótidos y muestra
#el resultado impreso en consola.

generar_secuencia_random<- function(n){
  nucleotidos<- c("A","T","G","C")
  sec_random<-c(n)
  
  for(i in 1:n){
    random_num<-runif(1, min = 1, max = 5)
   sec_random[i]<-nucleotidos[random_num]
  }
  print(sec_random)
  return(sec_random)
}

sec_random<-generar_secuencia_random(30)

