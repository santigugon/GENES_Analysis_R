#3.Crea una función que recibe una secuencia de DNA e imprime el porcentaje de
#cada base (A, C, G y T) en la secuencia. Ejecútala sobre la secuencia que generaste
#en el punto 1 y muestra el resultado impreso en consola.

calc_porcentaje<- function(sec){
  tam_secuencia<-length(sec)
  A<-0
  T<-0
  G<-0
  C<-0
  
  for(i in 1:tam_secuencia){
    if(sec[i]=="A"){
    A<-A+1  
    }
    else if(sec[i]=="T"){
      T<-T+1  
    }
    else if(sec[i]=="G"){
      G<-G+1  
    }
    else if(sec[i]=="C"){
      C<-C+1  
    }
  }
  cat("Porcentaje de cada base A:",((A/tam_secuencia)*100),"% T:",((T/tam_secuencia)*100),"% G:",((G/tam_secuencia)*100),"%  C:",((C/tam_secuencia)*100),"%")
  
}
calc_porcentaje(sec_random)

#4. Para representar una molécula de ADN de doble hebra basta con escribir la
#secuencia de una de sus hebras. Consideremos, por ejemplo, la secuencia
#TGCGATAC. Como no se indica lo contrario, se asume que la secuencia está escrita
#en sentido 5'→3' y, por lo tanto, se trata de la hebra directa (forward strand):
 
invertir_hebra<-function(sec){
  tam_secuencia<-length(sec)
  
  sec_invertida<-character(tam_secuencia)
  for(i in 1:tam_secuencia){
  sec_invertida[tam_secuencia+1-i]<-sec[i]  
  }
  print(sec_invertida)
}
